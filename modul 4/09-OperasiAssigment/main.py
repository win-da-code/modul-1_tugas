# operasi yang dapat dilakukan dengan meringkas
# operasi ditambah dengan assigment

a = 5 # assigment
print("nilai a = ", a)

# perjumlahan, pengurangan, pembagian, perkalian
# +=, -=, *=, /=
a += 1 # artinya a = a + 1
print("nilai a += 1, nilai a menjadi = ", a)

a -= 1 # artinya a = a - 1
print("nilai a -= 1, nilai a menjadi = ", a)

a *= 5 # artinya a = a *= 5
print("nilai a *= 5, nilai a menjadi = ", a)

a /= 5 # artinya a = a /= 5
print("nilai a /= 5, nilai a menjadi = ", a)


# modulus dan floor division dan epon
# %=, //=
a %= 3 # artinya a = a %= 3
print("nilai a %= 3, nilai a menjadi = ", a)

a //= 1 # artinya a = a //= 1
print("nilai a //= 5, nilai a menjadi = ", a)


# pangkat / ecponen
# **=
a **= 10 # artinya a = a **= 1
print("nilai a **= 5, nilai a menjadi = ", a)

# operator logika (OR)
c = True
print("nilai c = ", c)
c |= False # artinya c = c or false
print("nilai c |=False, nilai c menjadi = ", c)
c |= True # artinya c = c or true
print("nilai c |=true, nilai c menjadi = ", c)

# AND
c = True
print("nilai c = ", c)
c &= False # artinya c = c and false
print("nilai c &=False, nilai c menjadi = ", c)