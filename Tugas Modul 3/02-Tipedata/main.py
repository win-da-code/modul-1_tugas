# Tipe data untuk menghitung luas lingkaran
import math
# Nilai jari-jari (float)
r = 10.0
# Menghitung luas lingkaran (float)
luas_lingkaran = math.pi * r ** 2
print("Luas lingkaran adalah:", luas_lingkaran)

# Tipe data untuk konversi suhu
fahrenheit = 130.0  # Fahrenheit (float)
# Mengonversi suhu ke Celsius (float)
celsius = (fahrenheit - 32) * 5 / 9
print("Suhu dalam Celsius adalah:", celsius)


# Tipe data untuk menghitung luas segitiga
alas = 10.0  # Alas segitiga (float)
tinggi = 5.0  # Tinggi segitiga (float)
# Menghitung luas segitiga (float)
luas = 0.5 * alas * tinggi
print("Luas segitiga adalah:", luas)